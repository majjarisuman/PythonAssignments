#Right-Angle triangle
Rows = 5
for i in  range(1,Rows+1):
    print('*'*i)

rows = 5

for i in range(1, rows + 1):
    for j in range(i):
        print(i, end=" ")
    print()


height = 5
for i in range(1, height + 1):
        for j in range(1, i + 1):
            print(j, end=" ")
        print()
