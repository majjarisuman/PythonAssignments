# Logical operators are used to combine or modify logical statements.

#---------------- Logical Operators ------------------

# Logical and
x = True
y = False
result =  x and y 
print(result)   # Output: False

# Logical or
a = True
b = False
result1 = a or b
print(result1)  ## Output: True

# Logical NOT
p = True
result2 = not p
print(result2)   # Output: False

num = 25
result3 = (num > 10) and (num < 30)
print(result3)   # Output: True

age = 25
income = 50000
result4 = (age >= 18) and (income > 30000)
print(result4)   # Output: True

q = True
r = False
result5 = q != r
print(result5) # Output: True


value = 15
result6 = not (value > 20)
print(result6)   # Output: True








