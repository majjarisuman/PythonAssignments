word = "PyThon fuLL staCk coUrse"

print(word)   #PyThon fuLL staCk coUrse

#Upper
print(word.upper())  #PYTHON FULL STACK COURSE

#Lower
print(word.lower())  #python full stack course

#Swapcase
print(word.swapcase())  #pYtHON FUll STAcK COuRSE

#Capitalize
print(word.capitalize())   #Python full stack course

#Title
print(word.title())   #Python Full Stack Course

